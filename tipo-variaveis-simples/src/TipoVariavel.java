public class TipoVariavel {
    public static void main(String[] args) throws Exception {
        //Tipos de variaveis começando com Double 
        double salarioMinimo = 2500;

        //Byte
        byte idade = 30;
        
        //Short
        short numeroDePessoas = 10000;
        
        //Integer
        int numeroDeFuncionarios = 1000;
        
        //Long
        long numeroDeClientes = 1000000000;
        
        //Float
        float precoDeComida = 10.5f;
        
        //Boolean
        boolean usuarioLogado = true;
        
        //Char
        char letra = 'a';
        
        //String
        String nomeDoUsuario = "Juliana";

        //Constantes é necessario usar o final para definir a constant e manter em caixa alta
        final double PI = 3.14159;
        final int MAX_CLIENTES = 10000;

       
    }
}
